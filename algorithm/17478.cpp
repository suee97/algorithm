#include <bits/stdc++.h>
using namespace std;
int n, maxN;
string s1 = "\"재귀함수가 뭔가요?\"\n";
string s2 = "\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.\n";
string s3 = "마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.\n";
string s4 = "그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"\n";

string s5 = "라고 답변하였지.\n";

string s6 = "\"재귀함수는 자기 자신을 호출하는 함수라네\"\n";

void func(int n) {
    if (n == 0) return;
    string bar = "";
    for (int i = maxN-1; i >= n; i--) {
        bar += "____";
    }
    cout << bar + s1;
    if (n == 1) {
        cout << bar + s6;
    } else {
        cout << bar + s2;
        cout << bar + s3;
        cout << bar + s4;
    }

    func(n-1);
    cout << bar + s5;
    
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    cin >> n;
    maxN = n+1;
    cout << "어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.\n";
    func(n+1);
}